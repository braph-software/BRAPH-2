% test standardize
A = randn(randi([2 10]));

%% Test 1: Standardize Threshold
B = standardize(A);

C = A;
C(C<0)=0;
C(C>1)=1;

assert(isequal(B, C), ...
    'BRAPH:standardize:Bug', ...
    'Standardize case ''Threshold'' not working correctly')
assert(~any(B(:) < 0), ...
    'BRAPH:standardize:Bug', ...
    'Standardize case ''Threshold'' giving values < 0')
assert(~any(B(:) > 1), ...
    'BRAPH:standardize:Bug', ...
    'Standardize case ''Threshold'' giving values > 1')

%% Test 1: Standardize Range
B = standardize(A, 'StandardizeRule', 'range');

assert(isequal(B, (A - min(A(:))) / (max(A(:)) - min(A(:)))), ...
    'BRAPH:standardize:Bug', ...
    'Standardize case ''range'' not working correctly')
assert(~any(B(:) < 0), ...
    'BRAPH:standardize:Bug', ...
    'Standardize case ''range'' giving values < 0')
assert(~any(B(:) > 1), ...
    'BRAPH:standardize:Bug', ...
    'Standardize case ''range'' giving values > 1')