%% EXAMPLE_NNCV_CON_BUD_CLASSIFICATION_ADJACENCYMATRIX
% Script example pipeline for NN classification with the input of adjacency matrix 

clear variables %#ok<*NASGU>

%% Load brain atlas
im_ba = ImporterBrainAtlasXLS( ...
    'FILE', [fileparts(which('example_NN_CON_WU_Classification_AdjacencyMatrix')) filesep 'example data CON NN' filesep 'classification' filesep 'desikan_atlas.xlsx'], ...
    'WAITBAR', true ...
    );

ba = im_ba.get('BA');

%% Load groups of SubjectCON
im_gr1 = ImporterGroupSubjectCON_XLS( ...
    'DIRECTORY', [fileparts(which('example_NN_CON_WU_Classification_AdjacencyMatrix')) filesep 'example data CON NN' filesep 'classification' filesep 'xls' filesep 'GroupName1'], ...
    'BA', ba, ...
    'WAITBAR', true ...
    );

gr1 = im_gr1.get('GR');

im_gr2 = ImporterGroupSubjectCON_XLS( ...
    'DIRECTORY', [fileparts(which('example_NN_CON_WU_Classification_AdjacencyMatrix')) filesep 'example data CON NN' filesep 'classification' filesep 'xls' filesep 'GroupName2'], ...
    'BA', ba, ...
    'WAITBAR', true ...
    );

gr2 = im_gr2.get('GR');

%% Construct the dataset
nnd1 = NNData_CON_BUD( ...
    'GR', gr1, ...
    'INPUT_TYPE', 'adjacency_matrices', ...
    'TARGET_NAME', gr1.get('ID'), ...
    'DENSITIES', [50 100], ...
    'WAITBAR', true ...
    );

gr1_nn = nnd1.get('GR_NN');

nnd2 = NNData_CON_BUD( ...
    'GR', gr2, ...
    'INPUT_TYPE', 'adjacency_matrices', ...
    'TARGET_NAME', gr2.get('ID'), ...
    'DENSITIES', [50 100], ...
    'WAITBAR', true ...
    );

gr2_nn = nnd2.get('GR_NN');

%% Initiate the cross validation analysis
nncv = NNClassifierCrossValidation( ...
    'GR1', gr1_nn, ...
    'GR2', gr2_nn, ...
    'EPOCHS', 100, ...
    'KFOLD', 5 ...
    );

%% Evaluate the Performance
gr_cv = nncv.get('GR_PREDICTION');
auc_test = nncv.get('AUC');
cm_test = nncv.get('CONFUSION_MATRIX');
feature_importance = nncv.get('FEATURE_IMPORTANCE');

close all