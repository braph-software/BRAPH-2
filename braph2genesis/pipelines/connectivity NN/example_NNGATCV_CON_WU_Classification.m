%% EXAMPLE_NNGATCV_CON_WU_CLASSIFICATION
% Script example pipeline for GAT NN classification

clear variables %#ok<*NASGU>

%% Load brain atlas
im_ba = ImporterBrainAtlasXLS( ...
    'FILE', [fileparts(which('example_NNGATCV_CON_WU_Classification')) filesep 'example data CON (DTI)' filesep 'desikan_atlas.xlsx'], ...
    'WAITBAR', true ...
    );

ba = im_ba.get('BA');

%% Load groups of SubjectCON
im_gr1 = ImporterGroupSubjectCON_XLS( ...
    'DIRECTORY', [fileparts(which('example_NNGATCV_CON_WU_Classification')) filesep 'example data CON (DTI)' filesep 'xls' filesep 'GroupName1'], ...
    'BA', ba, ...
    'WAITBAR', true ...
    );

gr1 = im_gr1.get('GR');

im_gr2 = ImporterGroupSubjectCON_XLS( ...
    'DIRECTORY', [fileparts(which('example_NNGATCV_CON_WU_Classification')) filesep 'example data CON (DTI)' filesep 'xls' filesep 'GroupName2'], ...
    'BA', ba, ...
    'WAITBAR', true ...
    );

gr2 = im_gr2.get('GR');

%% Construct the dataset
nnd1 = NNDataGAT_CON_WU( ...
    'GR', gr1, ...
    'MEASURES', {'Degree'}, ...
    'TARGET_NAME', gr1.get('ID'), ...
    'WAITBAR', true ...
    );

gr1_nn = nnd1.get('GR_NN');

nnd2 = NNDataGAT_CON_WU( ...
    'GR', gr2, ...
    'MEASURES', {'Degree'}, ...
    'TARGET_NAME', gr2.get('ID'), ...
    'WAITBAR', true ...
    );

gr2_nn = nnd2.get('GR_NN');

%% Initiate the cross validation analysis
nncv = NNClassifierGATCrossValidation( ...
    'GR1', gr1_nn, ...
    'GR2', gr2_nn, ...
    'KFOLD', 5, ...
    'FEATURE_MASK', 0.0, ...
    'REPETITION', 1 ...
    );

%% Evaluate the Performance
gr_cv = nncv.get('GR_PREDICTION');
auc_test = nncv.get('AUC');
cm_test = nncv.get('CONFUSION_MATRIX');

close all