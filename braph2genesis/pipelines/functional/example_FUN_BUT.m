%EXAMPLE_FUN_BUT
% Script example pipeline FUN BUT

clear variables %#ok<*NASGU>

%% Load BrainAtlas
im_ba = ImporterBrainAtlasXLS( ...
    'FILE', [fileparts(which('example_FUN_BUT')) filesep 'example data FUN' filesep 'aal90_atlas.xlsx'], ...
    'WAITBAR', true ...
    );

ba = im_ba.get('BA');

%% Load Groups of SubjectFUN
im_gr1 = ImporterGroupSubjectFUN_XLS( ...
    'DIRECTORY', [fileparts(which('example_FUN_BUT')) filesep 'example data FUN' filesep 'xls' filesep 'GroupName1'], ...
    'BA', ba, ...
    'WAITBAR', true ...
    );

gr1 = im_gr1.get('GR');

im_gr2 = ImporterGroupSubjectFUN_XLS( ...
    'DIRECTORY', [fileparts(which('example_FUN_BUT')) filesep 'example data FUN' filesep 'xls' filesep 'GroupName2'], ...
    'BA', ba, ...
    'WAITBAR', true ...
    );

gr2 = im_gr2.get('GR');

%% Analysis FUN BUT
thresholds = 0.4:.2:1;

a_BUT1 = AnalyzeEnsemble_FUN_BUT( ...
    'GR', gr1, ...
    'THRESHOLDS', thresholds ...
    );

a_BUT2 = AnalyzeEnsemble_FUN_BUT( ...
    'GR', gr2, ...
    'THRESHOLDS', thresholds ...
    );

% measure calculation
clustering_BUT1 = a_BUT1.getMeasureEnsemble('Clustering').get('M');
clustering_av_BUT1 = a_BUT1.getMeasureEnsemble('ClusteringAv').get('M');

clustering_BUT2 = a_BUT2.getMeasureEnsemble('Clustering').get('M');
clustering_av_BUT2 = a_BUT2.getMeasureEnsemble('ClusteringAv').get('M');

% comparison
c_BUT = CompareEnsemble( ...
    'P', 10, ...
    'A1', a_BUT1, ...
    'A2', a_BUT2, ...
    'WAITBAR', true, ...
    'VERBOSE', false, ...
    'MEMORIZE', true ...
    );

clustering_BUT_diff = c_BUT.getComparison('Clustering').get('DIFF');
clustering_BUT_p1 = c_BUT.getComparison('Clustering').get('P1');
clustering_BUT_p2 = c_BUT.getComparison('Clustering').get('P2');
clustering_BUT_cil = c_BUT.getComparison('Clustering').get('CIL');
clustering_BUT_ciu = c_BUT.getComparison('Clustering').get('CIU');

clustering_av_BUT_diff = c_BUT.getComparison('ClusteringAv').get('DIFF');
clustering_av_BUT_p1 = c_BUT.getComparison('ClusteringAv').get('P1');
clustering_av_BUT_p2 = c_BUT.getComparison('ClusteringAv').get('P2');
clustering_av_BUT_cil = c_BUT.getComparison('ClusteringAv').get('CIL');
clustering_av_BUT_ciu = c_BUT.getComparison('ClusteringAv').get('CIU');
