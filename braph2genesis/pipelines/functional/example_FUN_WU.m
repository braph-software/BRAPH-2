%EXAMPLE_FUN_WU
% Script example pipeline FUN WU

clear variables %#ok<*NASGU>

%% Load BrainAtlas
im_ba = ImporterBrainAtlasXLS( ...
    'FILE', [fileparts(which('example_FUN_WU')) filesep 'example data FUN' filesep 'aal90_atlas.xlsx'], ...
    'WAITBAR', true ...
    );

ba = im_ba.get('BA');

%% Load Groups of SubjectFUN
im_gr1 = ImporterGroupSubjectFUN_XLS( ...
    'DIRECTORY', [fileparts(which('example_FUN_WU')) filesep 'example data FUN' filesep 'xls' filesep 'GroupName1'], ...
    'BA', ba, ...
    'WAITBAR', true ...
    );

gr1 = im_gr1.get('GR');

im_gr2 = ImporterGroupSubjectFUN_XLS( ...
    'DIRECTORY', [fileparts(which('example_FUN_WU')) filesep 'example data FUN' filesep 'xls' filesep 'GroupName2'], ...
    'BA', ba, ...
    'WAITBAR', true ...
    );

gr2 = im_gr2.get('GR');

%% Analysis FUN WU
a_WU1 = AnalyzeEnsemble_FUN_WU( ...
    'GR', gr1 ...
    );

a_WU2 = AnalyzeEnsemble_FUN_WU( ...
    'GR', gr2 ...
    );

% measure calculation
strength_WU1 = a_WU1.getMeasureEnsemble('Strength').get('M');
strength_av_WU1 = a_WU1.getMeasureEnsemble('StrengthAv').get('M');
distance_WU1 = a_WU1.getMeasureEnsemble('Distance').get('M');

strength_WU2 = a_WU2.getMeasureEnsemble('Strength').get('M');
strength_av_WU2 = a_WU2.getMeasureEnsemble('StrengthAv').get('M');
distance_WU2 = a_WU2.getMeasureEnsemble('Distance').get('M');

% comparison
c_WU = CompareEnsemble( ...
    'P', 10, ...
    'A1', a_WU1, ...
    'A2', a_WU2, ...
    'WAITBAR', true, ...
    'VERBOSE', false, ...
    'MEMORIZE', true ...
    );

strength_WU_diff = c_WU.getComparison('Strength').get('DIFF');
strength_WU_p1 = c_WU.getComparison('Strength').get('P1');
strength_WU_p2 = c_WU.getComparison('Strength').get('P2');
strength_WU_cil = c_WU.getComparison('Strength').get('CIL');
strength_WU_ciu = c_WU.getComparison('Strength').get('CIU');

modularity_av_WU_diff = c_WU.getComparison('Modularity').get('DIFF');
modularity_av_WU_p1 = c_WU.getComparison('Modularity').get('P1');
modularity_av_WU_p2 = c_WU.getComparison('Modularity').get('P2');
modularity_av_WU_cil = c_WU.getComparison('Modularity').get('CIL');
modularity_av_WU_ciu = c_WU.getComparison('Modularity').get('CIU');
