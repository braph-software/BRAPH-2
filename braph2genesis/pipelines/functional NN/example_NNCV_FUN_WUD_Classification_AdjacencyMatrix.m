%EXAMPLE_NNCV_FUN_WUD_CLASSIFICATION_ADJACENCYMATRIX
% Script example pipeline for NN classification with the input of adjacency matrix 


clear variables %#ok<*NASGU>

%% Load BrainAtlas
im_ba = ImporterBrainAtlasXLS( ...
    'FILE', [fileparts(which('example_NNCV_FUN_WUD_Classification_AdjacencyMatrix')) filesep 'example data FUN (fMRI)' filesep 'craddock_atlas.xlsx'], ...
    'WAITBAR', true ...
    );

ba = im_ba.get('BA');

%% Load Groups of SubjectFUN
im_gr1 = ImporterGroupSubjectFUN_XLS( ...
    'DIRECTORY', [fileparts(which('example_NNCV_FUN_WUD_Classification_AdjacencyMatrix')) filesep 'example data FUN (fMRI)' filesep 'xls' filesep 'GroupName1'], ...
    'BA', ba, ...
    'WAITBAR', true ...
    );

gr1 = im_gr1.get('GR');

im_gr2 = ImporterGroupSubjectFUN_XLS( ...
    'DIRECTORY', [fileparts(which('example_NNCV_FUN_WU_Classification_AdjacencyMatrix')) filesep 'example data FUN (fMRI)' filesep 'xls' filesep 'GroupName2'], ...
    'BA', ba, ...
    'WAITBAR', true ...
    );

gr2 = im_gr2.get('GR');

%% Construct the dataset
densities = [10];
nnd1 = NNData_FUN_WUD( ...
    'GR', gr1, ...
    'DENSITIES', densities, ...
    'INPUT_TYPE', 'adjacency_matrices', ...
    'TARGET_NAME', gr1.get('ID') ...
    );

gr1_nn = nnd1.get('GR_NN');

nnd2 = NNData_FUN_WUD( ...
    'GR', gr2, ...
    'DENSITIES', densities, ...
    'INPUT_TYPE', 'adjacency_matrices', ...
    'TARGET_NAME', gr2.get('ID') ...
    );

gr2_nn = nnd2.get('GR_NN');

%% Initiate the cross validation analysis
nncv = NNClassifierCrossValidation( ...
    'GR1', gr1_nn, ...
    'GR2', gr2_nn, ...
    'KFOLD', 5, ...
    'REPETITION', 1, ...
    'FEATURE_MASK', 0.05, ...
    'PLOT_CM', true, ...
    'PLOT_ROC', true, ...
    'PLOT_MAP', true ...
    );

%% Evaluate the Performance
gr_cv = nncv.get('GR_PREDICTION');
auc_test = nncv.get('AUC');
cm_test = nncv.get('CONFUSION_MATRIX');

close all