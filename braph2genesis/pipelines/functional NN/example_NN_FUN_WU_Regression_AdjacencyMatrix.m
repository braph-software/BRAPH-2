%% EXAMPLE_FUN_NN_WU_REGRESSION_ADJACENCYMATRIX
% Script example pipeline for NN classification with the input of graph measures

clear variables %#ok<*NASGU>

%% Load brain atlas
im_ba = ImporterBrainAtlasXLS( ...
    'FILE', [fileparts(which('example_NN_FUN_WU_Classification_AdjacencyMatrix')) filesep 'example data FUN (fMRI)' filesep 'classification' filesep 'aal90_atlas.xlsx'], ...
    'WAITBAR', true ...
    );

ba = im_ba.get('BA');

%% Load group of SubjectFUN
im_gr = ImporterGroupSubjectFUN_XLS( ...
    'DIRECTORY', [fileparts(which('example_NN_FUN_WU_Classification_AdjacencyMatrix')) filesep 'example data FUN (fMRI)' filesep 'regression' filesep 'xls' filesep 'GroupName'], ...
    'BA', ba, ...
    'WAITBAR', true ...
    );

gr = im_gr.get('GR');

%% Construct the dataset
nnd = NNData_FUN_WU( ...
    'GR', gr, ...
    'INPUT_TYPE', 'adjacency_matrices', ...
    'TARGET_NAME', 'age' ...
    );

gr_nn = nnd.get('GR_NN');

%% Split the dataset
nnds = NNRegressorDataSplit( ...
    'GR', gr_nn, ...
    'SPLIT', 0.2 ...
    );

gr_train = nnds.get('GR_TRAIN_FS');
gr_val = nnds.get('GR_VAL_FS');

%% Train the neural network regressor with the training set
regressor = NNRegressorDNN( ...
    'GR', gr_train, ...
    'EPOCHS', 100, ...
    'VERBOSE', false, ...
    'PLOT_TRAINING', false, ...
    'SHUFFLE', 'every-epoch' ...
    );

regressor.memorize('MODEL');

%% Evaluate the regressor for the training set
nne_train = NNRegressorEvaluator( ...
    'GR', gr_train, ...
    'NN', regressor ...
    );

rmse_train = nne_train.get('RMSE');

%% Evaluate the regressor for the validation set
nne_val = NNRegressorEvaluator( ...
    'GR', gr_val, ...
    'NN', regressor ...
    );

rmse_val = nne_val.get('RMSE');
feature_importance = nne_val.get('FEATURE_PERMUTATION_IMPORTANCE');

close all