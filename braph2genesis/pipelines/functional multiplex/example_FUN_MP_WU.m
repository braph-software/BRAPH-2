%EXAMPLE_FUN_MP_WU
% Script example pipeline FUN MP WU

clear variables %#ok<*NASGU>

%% Load BrainAtlas
im_ba = ImporterBrainAtlasXLS( ...
    'FILE', [fileparts(which('example_FUN_MP_WU')) filesep 'example data FUN_MP (fMRI)' filesep 'aal90_atlas.xlsx'], ...
    'WAITBAR', true ...
    );

ba = im_ba.get('BA');

%% Load Groups of SubjectFUN_MP
im_gr1 = ImporterGroupSubjectFUN_MP_XLS( ...
    'DIRECTORY', [fileparts(which('example_FUN_MP_WU')) filesep 'example data FUN_MP (fMRI)' filesep 'xls' filesep 'GroupName1'], ...
    'BA', ba, ...
    'WAITBAR', true ...
    );

gr1 = im_gr1.get('GR');

im_gr2 = ImporterGroupSubjectFUN_MP_XLS( ...
    'DIRECTORY', [fileparts(which('example_FUN_MP_WU')) filesep 'example data FUN_MP (fMRI)' filesep 'xls' filesep 'GroupName2'], ...
    'BA', ba, ...
    'WAITBAR', true ...
    );

gr2 = im_gr2.get('GR');

%% Analysis FUN_MP WU
a_WU1 = AnalyzeEnsemble_FUN_MP_WU( ...
    'GR', gr1 ...
    );

a_WU2 = AnalyzeEnsemble_FUN_MP_WU( ...
    'GR', gr2 ...
    );

% measure calculation
wmpc_WU1 = a_WU1.getMeasureEnsemble('WeightedMultiplexParticipation').get('M');
wmpc_av_WU1 = a_WU1.getMeasureEnsemble('WeightedMultiplexParticipationAv').get('M');
weo_WU1 = a_WU1.getMeasureEnsemble('WeightedEdgeOverlap').get('M');

wmpc_WU2 = a_WU2.getMeasureEnsemble('WeightedMultiplexParticipation').get('M');
wmpc_av_WU2 = a_WU2.getMeasureEnsemble('WeightedMultiplexParticipationAv').get('M');
weo_WU2 = a_WU2.getMeasureEnsemble('WeightedEdgeOverlap').get('M');

% comparison
c_WU = CompareEnsemble( ...
    'P', 10, ...
    'A1', a_WU1, ...
    'A2', a_WU2, ...
    'WAITBAR', true, ...
    'VERBOSE', false, ...
    'MEMORIZE', true ...
    );

wmpc_WU_diff = c_WU.getComparison('WeightedMultiplexParticipation').get('DIFF');
wmpc_WU_p1 = c_WU.getComparison('WeightedMultiplexParticipation').get('P1');
wmpc_WU_p2 = c_WU.getComparison('WeightedMultiplexParticipation').get('P2');
wmpc_WU_cil = c_WU.getComparison('WeightedMultiplexParticipation').get('CIL');
wmpc_WU_ciu = c_WU.getComparison('WeightedMultiplexParticipation').get('CIU');

wmpc_av_WU_diff = c_WU.getComparison('WeightedMultiplexParticipationAv').get('DIFF');
wmpc_av_WU_p1 = c_WU.getComparison('WeightedMultiplexParticipationAv').get('P1');
wmpc_av_WU_p2 = c_WU.getComparison('WeightedMultiplexParticipationAv').get('P2');
wmpc_av_WU_cil = c_WU.getComparison('WeightedMultiplexParticipationAv').get('CIL');
wmpc_av_WU_ciu = c_WU.getComparison('WeightedMultiplexParticipationAv').get('CIU');