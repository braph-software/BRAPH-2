%% ¡header!
SettingsGraph < Settings (st, colormap settings) contains the graph settings.

%%% ¡description!
SettingsGraph provides the settings for an graph

%%% ¡seealso!
PanelFig, GUIFig, uiaxes

%% ¡props!

%%% ¡prop!
WEIGHTED (figure, logical) determines whether the weighted plot is shown.
%%%% ¡default!
true

%%% ¡prop!
COLORBAR (figure, logical) determines whether the colorbar is shown.
%%%% ¡default!
true

%%% ¡prop!
BINARY (figure, logical) determines whether the binary plot is shown.
%%%% ¡default!
true

%%% ¡prop!
BINARY_VALUE (figure, scalar) determines the value of the binary value.
%%%% ¡default!
0

%%% ¡prop!
HIST (figure, logical) determines whether the histogram plot is shown.
%%%% ¡default!
true
