%TEST_CATEGORY

category_list = Category.getCategories();

%% Test 1: Static methods
assert(numel(category_list) == Category.getCategoryNumber(), ...
    [BRAPH2.STR ':Category:' BRAPH2.WRONG_OUTPUT], ...
    'Category.getCategoryNumber() does not return the correct number of categories.')

for i = 1:1:numel(category_list)
    category = category_list{i};

    assert(Category.existsCategory(category), ...
        [BRAPH2.STR ':Category:' BRAPH2.WRONG_OUTPUT], ...
        ['Category.existsCategory(' tostring(category) ') must return true.'])

    assert(strcmp(eval(['Category.' upper(Category.getCategoryName(category)) '_NAME']), Category.getCategoryName(category)), ...
        [BRAPH2.STR ':Category:' BRAPH2.WRONG_OUTPUT], ...
        ['Category.getCategoryName(' tostring(category) ') must return a char array.'])

    assert(strcmp(eval(['Category.' upper(Category.getCategoryName(category)) '_DESCRIPTION']), Category.getCategoryDescription(category)), ...
        [BRAPH2.STR ':Category:' BRAPH2.WRONG_OUTPUT], ...
        ['Category.getCategoryDescription(' tostring(category) ') must return a char array.'])
end