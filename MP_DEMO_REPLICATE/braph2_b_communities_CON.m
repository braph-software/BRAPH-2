gr2_men_CON = im_gr2.get('GR');

%% Pipeline Connectivity Group Average Analysis WU 
% ITERATING COMMUNITIES
N = 246;
gamma = 1.1; %[1, 1.01, 1.02];
cs_combination_women_CON = zeros(N, 1);
cs_combination_men_CON = zeros(N, 1);

count = 1;
for i = gamma

        analysis_women = AnalyzeGroup_CON_GA_WU( ...
            'GR', gr1_women_CON ...
            );

        analysis_men = AnalyzeGroup_CON_GA_WU( ...
            'GR', gr2_men_CON ...
            );
        
        g1 = analysis_women.memorize('G');
        measurement_mcs1 = g1.get('MEASURE', 'CommunityStructure', 'GAMMA', gamma, 'RULE', 'louvain');
        S1 = cell2mat(measurement_mcs1.memorize('M'));
        
        g2 = analysis_men.memorize('G');
        measurement_mcs2 = g2.get('MEASURE', 'CommunityStructure', 'GAMMA', gamma, 'RULE', 'louvain');
        S2 = cell2mat(measurement_mcs2.memorize('M'));

        cs_combination_women_CON(:, count) = S1;
        cs_combination_men_CON(:, count) = S2;
        count = count + 1;
end

save('CON_communities.mat')

