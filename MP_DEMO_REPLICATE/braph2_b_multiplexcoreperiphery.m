    'GR_CON', gr1_women_CON, ...
    'GR_FUN', gr1_women_FUN ...
    );

gr1_women = co_gr1.get('GR_CON_FUN_MP');  % Women group

co_gr2 = CombineGroups_CON_FUN_MP( ...
    'GR_CON', gr2_men_CON, ...
    'GR_FUN', gr2_men_FUN ...
    );

gr2_men = co_gr2.get('GR_CON_FUN_MP');  % Men group

%% Pipeline Connectivity-Functional Multiplex BUD Analysis
densities = 5:0.5:9; % the results figure show results at density 9%

analysis_women = AnalyzeEnsemble_CON_FUN_MP_BUD( ...
    'GR', gr1_women, ...
    'DENSITIES', densities);

analysis_men = AnalyzeEnsemble_CON_FUN_MP_BUD( ...
    'GR', gr2_men, ...
    'DENSITIES', densities);

% measure calculation: Multiplex Core-Periphery 
mcoreperiphery_g1_ensemble_bud = analysis_women.get('MEASUREENSEMBLE', 'MultiplexCP').memorize('M');
mcoreperiphery_g2_ensemble_bud = analysis_men.get('MEASUREENSEMBLE', 'MultiplexCP').memorize('M');

%% Pipeline Connectivity-Functional Multiplex BUD Comparison 
c_BUD = CompareEnsemble( ...
    'P', 10000, ...
    'A1', analysis_women, ...
    'A2', analysis_men, ...
    'WAITBAR', false, ...
    'VERBOSE', false, ...
    'MEMORIZE', true ...
    );

% comparing: Multiplex Core-Periphery 
mcore_BUD_p1 = c_BUD.get('COMPARISON', 'MultiplexCP').memorize('P1');
mcore_BUD_p2 = c_BUD.get('COMPARISON', 'MultiplexCP').memorize('P2');
mcore_BUD_cil = c_BUD.get('COMPARISON', 'MultiplexCP').memorize('CIL');
mcore_BUD_ciu = c_BUD.get('COMPARISON', 'MultiplexCP').memorize('CIU');
mcore_BUD_diff = c_BUD.get('COMPARISON', 'MultiplexCP').memorize('DIFF');

save('MP_coreperiphery.mat')

