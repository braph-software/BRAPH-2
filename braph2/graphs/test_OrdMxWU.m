					'OrdMxWU.copy() does not work. The copied value of an EVANESCENT property must be NoValue.' ...
					)
			otherwise
				if isa(value, 'NoValue')
					assert( ...
						value_copy == value, ...
						[BRAPH2.STR ':OrdMxWU:' BRAPH2.FAIL_TEST], ...
						'OrdMxWU.copy() does not work. There should be a unique instance of NoValue.' ...
						)
				elseif isa(value, 'Element')
					assert( ...
						value_copy ~= value, ...
						[BRAPH2.STR ':OrdMxWU:' BRAPH2.FAIL_TEST], ...
						'OrdMxWU.copy() does not work. The handle of a copied element should point to a copied element.' ...
						)
