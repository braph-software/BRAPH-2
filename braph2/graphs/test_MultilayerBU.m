	%             'MultilayerBU Randomize is not functioning well.')
	    end
	
	    assert(isequal(numel(find(A2{i, i})), numel(find(random_A{i, i}))), ... % check same number of nodes
	        [BRAPH2.STR ':MultilayerBU:' BRAPH2.FAIL_TEST], ...
	        'MultilayerBU Randomize is not functioning well.')
	
