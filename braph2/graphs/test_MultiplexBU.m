	%             [BRAPH2.STR ':MultiplexBU:' BRAPH2.FAIL_TEST], ...
	%             'MultiplexBU Randomize is not functioning well.')
	    end
	
	    assert(isequal(numel(find(A2{i, i})), numel(find(random_A{i, i}))), ... % check same number of nodes
