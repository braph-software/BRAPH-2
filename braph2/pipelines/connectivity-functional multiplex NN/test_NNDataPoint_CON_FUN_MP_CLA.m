	        r = 0 + (0.5 - 0)*rand(size(A_full2));
	        diffA = A_full2 - r;
	        A_full2(A_full2 ~= 0) = diffA(A_full2 ~= 0);
	        % make the adjacency matrix symmetric
	        A_full2 = max(A_full2, transpose(A_full2));
