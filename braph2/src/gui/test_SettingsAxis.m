			[BRAPH2.STR ':SettingsAxis:' BRAPH2.FAIL_TEST], ...
			'SettingsAxis.encodeJSON() or SettingsAxis.decodeJSON() does not work. A JSON encoded/decoded element must have the same property values of the original element.' ...
			)
	end
end

