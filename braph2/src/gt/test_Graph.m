	for prop = 1:1:Graph.getPropNumber()
	 
		% excluded props
		if any(prop == [Graph.TEMPLATE Graph.PFGA Graph.PFGH])
			continue
		end
	 
		TAG = upper(Graph.getPropTag(prop));
		switch Graph.getPropCategory(prop)
			case {Category.PARAMETER}
				if prop == Graph.TEMPLATE
					assert( ...
						isa(g.getr(prop), 'Graph'), ...
						[BRAPH2.STR ':Graph:' BRAPH2.FAIL_TEST], ...
						'Graph TEMPLATE does not work. Graph.getr(''TEMPLATE'') should be a Graph element.' ...
						)
					assert( ...
						isa(g_template.getr(prop), 'NoValue'), ...
						[BRAPH2.STR ':Graph:' BRAPH2.FAIL_TEST], ...
						'Graph TEMPLATE does not work. Graph_template.getr(''TEMPLATE'') should be a NoValue.' ...
						)
				else
					assert( ...
						isa(g.getr(prop), 'Callback'), ...
						[BRAPH2.STR ':Graph:' BRAPH2.FAIL_TEST], ...
						['Graph TEMPLATE does not work. Graph.getr(' TAG ') should be a Callback.'] ...
						)
					assert( ...
						g.getr(prop).get('EL') == g_template && g.getr(prop).get('PROP') == prop && strcmp(g.getr(prop).get('TAG'), TAG), ...
						[BRAPH2.STR ':Graph:' BRAPH2.FAIL_TEST], ...
						['Graph TEMPLATE does not work. Graph.getr(' TAG ') should be a Callback to g_template.' TAG '.'] ...
						)
					assert( ...
						isa(g_template.getr(prop), 'NoValue'), ...
						[BRAPH2.STR ':Graph:' BRAPH2.FAIL_TEST], ...
						['Graph TEMPLATE does not work. Graph_template.getr(' TAG ') should be a NoValue.'] ...
						)
				end
			otherwise
				assert( ...
					isa(g.getr(prop), 'NoValue'), ...
					[BRAPH2.STR ':Graph:' BRAPH2.FAIL_TEST], ...
					['Graph TEMPLATE does not work. Graph.getr(' TAG ') should be a NoValue.'] ...
					)
				assert( ...
					isa(g_template.getr(prop), 'NoValue'), ...
					[BRAPH2.STR ':Graph:' BRAPH2.FAIL_TEST], ...
					['Graph TEMPLATE does not work. Graph_template.getr(' TAG ') should be a NoValue.'] ...
					)
		end
	end
end

