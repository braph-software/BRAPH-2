		OverlappingSIn.G, OverlappingSIn.getPropDefault(OverlappingSIn.G), ...
		OverlappingSIn.M, OverlappingSIn.getNoValue(), ...
		OverlappingSIn.PFM, OverlappingSIn.getPropDefault(OverlappingSIn.PFM) ...
		);
	 
	[json, struct, el_list] = encodeJSON(m);
	[m_dec, struct_dec, el_list_dec] = Element.decodeJSON(json);
	 
	assert(m_dec ~= m, ...
		[BRAPH2.STR ':OverlappingSIn:' BRAPH2.FAIL_TEST], ...
		'OverlappingSIn.encodeJSON() or OverlappingSIn.decodeJSON() does not work. A JSON encoded/decoded element must point to an element other than the original one.' ...
		)
	if all(cellfun(@(prop) OverlappingSIn.getPropFormat(prop), num2cell(OverlappingSIn.getProps())) == Format.HANDLE) && all(cellfun(@(prop) OverlappingSIn.getPropFormat(prop), num2cell(OverlappingSIn.getProps())) == Format.NET)
		assert(isequal(m_dec, m), ...
			[BRAPH2.STR ':OverlappingSIn:' BRAPH2.FAIL_TEST], ...
			'OverlappingSIn.encodeJSON() or OverlappingSIn.decodeJSON() does not work. A JSON encoded/decoded element must have the same property values of the original element.' ...
			)
	end
end

