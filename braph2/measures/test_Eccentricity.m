		)
	if all(cellfun(@(prop) Eccentricity.getPropFormat(prop), num2cell(Eccentricity.getProps())) == Format.HANDLE) && all(cellfun(@(prop) Eccentricity.getPropFormat(prop), num2cell(Eccentricity.getProps())) == Format.NET)
		assert(isequal(m_dec, m), ...
			[BRAPH2.STR ':Eccentricity:' BRAPH2.FAIL_TEST], ...
			'Eccentricity.encodeJSON() or Eccentricity.decodeJSON() does not work. A JSON encoded/decoded element must have the same property values of the original element.' ...
			)
	end
end

